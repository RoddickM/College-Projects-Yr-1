print("The addition operator or + adds two integers together.")
print("The sum of 5 + 3 is:", 5 + 3)

print("\nThe subtraction operator or - take away one value from another.")
print("The result of 5 - 3:", 5 - 3)

print("\nThe division operator or / divides two numbers.")
print("The result of 6 / 3:", int(6 / 3))

print("\nThe multiplication operator or * multiply two numbers together.")
print("The result of 5 * 3:", 5 * 3)

print("\nThe modulo operator or % outputs the remainder of two numbers that are divided.")
print("The result of 5 % 3:", 5 % 3)

print("\nThe floor division operator or // round the answer down to the nearest whole number.")
print("The result of 5 // 3:", 5 // 3)
